/** Obtiene la traducción para una clave en el idioma dado */
export function t(lang, key) {
  return translations[lang]?.[key] || translations.es[key] || key;
}

/** Detecta el idioma preferido del navegador (solo es/en) */
export function detectLang() {
  const stored = localStorage.getItem('artefactos_lang');
  if (stored && (stored === 'es' || stored === 'en')) return stored;
  const nav = navigator.language || navigator.userLanguage || 'es';
  return nav.startsWith('en') ? 'en' : 'es';
}

/** Guarda el idioma seleccionado */
export function setLang(lang) {
  localStorage.setItem('artefactos_lang', lang);
}
